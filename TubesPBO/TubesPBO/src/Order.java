    void Simpan_Data(){
        try{
            if(txtnomor.getText().equals("") ||txtnip.getText().equals("")){
                JOptionPane.showMessageDialog(rootPane, "DataTidak Boleh Kosong", "Pesan", JOptionPane.ERROR_MESSAGE);
            }else{
                String sql = "INSERT INTO riwayat (No_Rumah,NIP) VALUES ('"+txtnomor.getText()+"','"+txtnip.getText()+"')";
                java.sql.Connection conn = (java.sql.Connection)KoneksiDB.koneksi();
                java.sql.PreparedStatement pstm = conn.prepareCall(sql);
                pstm.execute();
                JOptionPane.showMessageDialog(null, "Proses Pembelian Berhasil");
                kembali();
                
            }
        } catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    private void kembali(){
        ListRumah n = new ListRumah();
        n.setVisible(true);
        this.setVisible(false);
    }
    
