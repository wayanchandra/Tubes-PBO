    private void tampilkan_datarumah(){
        DefaultTableModel model2 = new DefaultTableModel();
        model2.addColumn("No_Rumah");
        
        try{
            String sql = "SELECT No_Rumah FROM rumah";
            java.sql.Connection conn = (java.sql.Connection)KoneksiDB.koneksi();
            java.sql.PreparedStatement pstm = conn.prepareCall(sql);
            java.sql.ResultSet res = pstm.executeQuery(sql);
            
            while(res.next()){
                model2.addRow(new Object[]{res.getString(1)});
            }

            tabelnomor.setModel(model2);
            
        }catch (SQLException e){
            System.out.println("Error" + e.getMessage());
        }
    }
    
