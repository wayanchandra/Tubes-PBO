    void CekKosong(){
        try{
            if(txtnik.getText().equals("") ||txtnama.getText().equals("") ||txtemail.getText().equals("") ||txttelpon.getText().equals("") ||txtalamat.getText().equals("") ||txtpass.getText().equals("")){
                JOptionPane.showMessageDialog(rootPane, "DataTidak Boleh Kosong", "Pesan", JOptionPane.ERROR_MESSAGE);
                txtnik.requestFocus();
                kosongkan_form();
            }else{
                String sql = "INSERT INTO pembeli VALUES ('"+txtnama.getText()+"','"+txtemail.getText()+"','"+txttelpon.getText()+"','"+txtalamat.getText()+"','"+txtpass.getText()+"')";
                java.sql.Connection conn = (java.sql.Connection)KoneksiDB.koneksi();
                java.sql.PreparedStatement pstm = conn.prepareCall(sql);
                pstm.execute();
                JOptionPane.showMessageDialog(null, "Proses Simpan Akun Berhasil");
                //tampilkan_data();
                kosongkan_form();
                kembali();
            }
        } catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    private void kosongkan_form(){
        txtnama.setText(null);
        txtemail.setText(null);
        txttelpon.setText(null);
        txtalamat.setText(null);
        txtpass.setText(null);
    }
    private void kembali(){
        MenuLogin n = new MenuLogin();
        n.setVisible(true);
        this.setVisible(false);
    }
    
